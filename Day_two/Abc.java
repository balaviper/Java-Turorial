package Day_two;

public class Abc {

    public static void main(String[] args) {
        

        //Swapping concept  
        char c ='h';
        char s = 's';
        System.out.println("Before Swapping " + c +" "+ s);
        char temp = c;
        c = s;
        s = temp;
        System.out.println("After Swapping " + c +" "+ s);


    }
    
}
